def adaptive_unsharp_mask(img, amount=1.2, threshold=0):
    """Professional sharpening (Unsharp Mask) - crisp but not overpowered"""
    img_float = img.astype(np.float32)
    # Target 3x3 gaussian for high-frequency extraction
    blurred = cv2.GaussianBlur(img_float, (3, 3), 1.0)
    sharpened = float(amount + 1) * img_float - float(amount) * blurred
    sharpened = np.clip(sharpened, 0, 255).astype(np.uint8)

    if threshold > 0:
        low_contrast_mask = np.absolute(img_float - blurred) < threshold
        np.copyto(sharpened, img, where=low_contrast_mask)

    return sharpened

def crisp_visibility_amplify(img, smoothed_mean):
    """Visibility enhancement using smoothed metrics to kill flicker"""
    img = img.astype(np.float32) / 255.0

    # Use smoothed_mean instead of per-frame mean to ensure zero flicker
    if smoothed_mean < 0.35:
        gamma = 0.88
    elif smoothed_mean < 0.50:
        gamma = 0.94
    else:
        gamma = 1.0

    img = np.power(img, gamma)

    # Balanced contrast stretch
    p1, p99 = np.percentile(img, (1.2, 98.8))
    img = np.clip((img - p1) / (p99 - p1 + 1e-6), 0, 1)

    return (img * 255).astype(np.uint8)

def balanced_color_correction(img):
    """Deep natural color stabilization"""
    img = img.astype(np.float32)
    avg = img.mean(axis=(0, 1))
    gray = avg.mean()

    # Gentle correction to preserve natural forest look
    for c in range(3):
        target = gray / (avg[c] + 1e-6)
        img[:, :, c] *= (0.7 + 0.3 * target) # Weighted toward original

    img = np.clip(img, 0, 255).astype(np.uint8)

    # Adaptive saturation boost
    hsv = cv2.cvtColor(img, cv2.COLOR_RGB2HSV).astype(np.float32)
    sat_mean = hsv[:, :, 1].mean()

    boost = 1.35 if sat_mean < 50 else 1.22 if sat_mean < 75 else 1.12
    hsv[:, :, 1] = np.clip(hsv[:, :, 1] * boost, 0, 255)

    return cv2.cvtColor(hsv.astype(np.uint8), cv2.COLOR_HSV2RGB)

