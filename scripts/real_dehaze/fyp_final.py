print("✨ INITIALIZING CRISP DEHAZING ENGINE...\\n")

MEAN = torch.tensor([0.485, 0.456, 0.406]).view(1, 1, 3)
STD = torch.tensor([0.229, 0.224, 0.225]).view(1, 1, 3)
if torch.cuda.is_available():
    MEAN, STD = MEAN.cuda(), STD.cuda()

cap = cv2.VideoCapture("hazy_video.mp4")
fps = cap.get(cv2.CAP_PROP_FPS)
width = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

fourcc = cv2.VideoWriter_fourcc(*'mp4v')
out = cv2.VideoWriter("dehazed_crisp.mp4", fourcc, fps, (width, height))

prev_frame = None
pbar = tqdm(total=total_frames, desc="💎 Crisp & Clear")

# EMA Metrics to kill flickering (Exponential Moving Average)
ema_mean = None
ema_alpha = 0.15 # 15% current, 85% history for massive stability

frame_count = 0
while True:
    ret, frame = cap.read()
    if not ret:
        break

    frame_count += 1
    rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
    h, w, _ = rgb.shape

    # 8-pixel alignment
    new_h, new_w = (h + 7) // 8 * 8, (w + 7) // 8 * 8
    pad_h, pad_w = new_h - h, new_w - w

    # Normalize
    inp = torch.from_numpy(rgb / 255.0).float()
    if torch.cuda.is_available():
        inp = inp.cuda()
    inp = (inp - MEAN) / STD
    inp = inp.permute(2, 0, 1).unsqueeze(0)
    inp = F.pad(inp, (0, pad_w, 0, pad_h), mode='reflect')

    # Inference
    with torch.no_grad():
        out_tensor = model(inp)[:, :3]

    # Denormalize
    out_tensor = out_tensor[:, :, :h, :w].squeeze(0).permute(1, 2, 0)
    out_tensor = (out_tensor * STD) + MEAN
    out_tensor = out_tensor.clamp(0, 1)

    # Aggressive but crystal clear model boost
    out_tensor = torch.pow(out_tensor, 0.82)
    out_tensor = out_tensor.clamp(0, 1)

    result = (out_tensor.cpu().numpy() * 255.0).astype(np.uint8)

    # --- CRISP POST-PROCESSING ---

    # 1. Update EMA Metrics (Prevents jumping/flickering)
    current_mean = result.mean() / 255.0
    if ema_mean is None:
        ema_mean = current_mean
    else:
        ema_mean = (ema_alpha * current_mean) + (1 - ema_alpha) * ema_mean

    # 2. Enhance Visibility using stable metrics
    result = crisp_visibility_amplify(result, ema_mean)

    # 3. Apply Unsharp Mask for professional crispness
    result = adaptive_unsharp_mask(result, amount=0.6, threshold=2) # Gentle but sharp

    # 4. Stabilize Color
    result = balanced_color_correction(result)

    # 5. Lightweight Grid Removal (d=3 prevents MUSHY texture)
    result = cv2.bilateralFilter(result, d=3, sigmaColor=35, sigmaSpace=25)

    # --- TEMPORAL CONSISTENCY ---
    if prev_frame is not None:
        # 0.85/0.15 blend to keep results SHARP (stops ghosting/mush)
        result = cv2.addWeighted(result, 0.85, prev_frame, 0.15, 0)
    prev_frame = result.copy()

    out.write(cv2.cvtColor(result, cv2.COLOR_RGB2BGR))
    pbar.update(1)

    if frame_count % 30 == 0:
        del inp, out_tensor
        if torch.cuda.is_available():
            torch.cuda.empty_cache()
        gc.collect()

pbar.close()
cap.release()
out.release()
print(f"\\n✅ Professional Crisp Dehazing Done! Processed {frame_count} frames.")

